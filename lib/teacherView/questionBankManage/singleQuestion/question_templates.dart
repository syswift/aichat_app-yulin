class QuestionTemplate {
  static String getTemplate(String type, {int index = 1}) {
    switch (type) {
      case '单选题':
        return '''${index == 1 ? '一、选择题\n' : ''}
$index. 题目：


A. 
B. 
C. 
D. 

答案：
提示：

----------------------------------------
''';

      case '判断题':
        return '''${index == 1 ? '一、判断题\n' : ''}
$index. 请判断下面的说法是对还是错：


答案：
提示：

----------------------------------------
''';

      case '多选题':
        return '''${index == 1 ? '一、多选题\n' : ''}
$index. 题目：


A. 
B. 
C. 
D. 

答案：（可以选择多个哦）
提示：

----------------------------------------
''';

      case '看图识字':
        return '''${index == 1 ? '一、看图识字\n' : ''}
$index. [点击这里添加图片]

请根据图片回答问题：

1. 
2. 
3. 

生字：
词语：

----------------------------------------
''';

      case '音频题':
        return '''${index == 1 ? '一、听力练习\n' : ''}
$index. [点击这里添加音频]

听一听，回答问题：

1. 
2. 
3. 

新学单词：
温馨提示：

----------------------------------------
''';

      case '视频题':
        return '''${index == 1 ? '一、观看视频\n' : ''}
$index. [点击这里添加视频]

看一看，想一想：

1. 
2. 
3. 

学习重点：
拓展知识：

----------------------------------------
''';

      default:
        return '';
    }
  }
}